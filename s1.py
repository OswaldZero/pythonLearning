s="0123456789"
#截取第一位到第三位的字符,结果:012
print("s[0:3]:",s[0:3])
#截取字符串的全部字符,结果:0123456789
print("s[:]:",s[:])
#截取第六个字符到最后一个字符,结果:6789
print("s[6:]:",s[6:])
#截取从头开始到倒数第三个字符之前(不包括第三个),结果:0123456
print("s[,-3]:",s[:-3])
#截取第三个字符,结果:2
print("s[2]:",s[2])
#截取最后一个字符,结果:9
print("s[-1]:",s[-1])
#创建一个与源字符相反的字符,结果:9876543210
print("s[::-1]:",s[::-1])
#截取倒数第三个与倒数第一位之前的字符,结果:78
print("s[-3:-1]:",s[-3:-1])
#截取倒数第三位到结尾,结果:789
print("s[-3:]:",s[-3:])
#逆序截取,结果:96
print("s[:-5:-3]",s[:-5:-3])